// 缩放坐标到指定Canvas尺寸
function scalePath(path, canvasWidth, canvasHeight) {
  const scaleX = canvasWidth / MAP_WIDTH
  const scaleY = canvasHeight / MAP_HEIGHT
  return path.map(([x, y]) => [x * scaleX, y * scaleY])
}

function getProvinceData(canvasWidth, canvasHeight) {
  const result = {}
  Object.keys(CHINA_MAP_DATA).forEach(name => {
    const item = CHINA_MAP_DATA[name]
    result[name] = {
      name,
      center: [item.center[0] * canvasWidth / MAP_WIDTH, item.center[1] * canvasHeight / MAP_HEIGHT],
      path: scalePath(item.path, canvasWidth, canvasHeight)
    }
  })
  return result
}

